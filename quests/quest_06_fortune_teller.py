# Quest 6: The Fortune Teller
# Concept: input() - How to ask the user a question and get their answer.
# input() pauses the program and waits for the user to type something.

# Ask the user for their name — input() always returns a string
name = input("What is your name, traveller? ")

# Ask the user for their quest
quest = input("And what is your quest? ")

# Print a confirmation message using the information the user provided
print(f"\nAh, {name}! Your quest to '{quest}' has been noted.")
print("The stars have been consulted. Your journey will be legendary!")
